week = prompt('请输入星期')
week = parseInt(week)

switch (week) {
    case 1:
        alert('新的一周')
        break
    case 2:
        alert('明天又学习')
        break
    case 3:
        alert('算算有1日假期')
        break
    case 4:
        alert('还剩半天假')
        break
    case 5:
        alert('2.5日假期')
        break
    case 6:
        alert('今天干点什么')
        break
    case 7:
        alert('歇一天吧')
        break
    default:
        alert('???')
}