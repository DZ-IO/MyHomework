// 蜘蛛有8条腿，蜻蜓有6条腿和2对翅膀，蝉有6条腿和1对翅膀。
// 这三种小虫共18只，有118条腿和20对翅膀.每种小虫各几只?

// 外层穷举蜘蛛
for (zz = 1; zz <= 18; zz++) {
    // 内层穷举蜻蜓
    for (qt = 1; qt <= 10; qt++) {
        // 蝉是算出来的
        c = 18 - zz - qt;
        // 判断结果
        if (zz * 8 + qt * 6 + c * 6 == 118 && 2 * qt + c == 20) {
            // 输出
            document.write('蜻蜓有' + qt + '只，蜘蛛有' + zz + '只，蝉有' + c + '只');
        }
    }
}